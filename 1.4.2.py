#Gör ett program som har följande meny:
#[L]ägg till temperaturmätning
#[S]kriv ut alla temperaturer och medeltemperatur
#[T]a bort temperaturmätning
#[A]vsluta

#Låt användaren får mata in temperaturmätningar ifrån en väderstation under valet “Lägg till temperaturmätning”. 
#Varje temperaturmätning skall sparas i en lista i programmet. 
#När användaren väljer valet “Skriv ut alla temperaturer och medeltemperatur” skall hela listan skrivas ut samt medeltemperaturen. 
#Användaren skall även under valet “Ta bort temperaturmätning” kunna ta bort en temperaturmätning ifall den vill.
#Programmet skall inte avslutas förens användaren anger “Avsluta” i menyn.
